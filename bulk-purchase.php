<script>
    // Bulk Order Form Validation and AJAX Submission
    $('#bulk-order-form').on('submit', function (e) {
        e.preventDefault();

        const name = $('#bulk-name').val().trim();
        const email = $('#bulk-email').val().trim();
        const phone = $('#bulk-phone').val().trim();
        const company = $('#bulk-company').val().trim();
        const category = $('#bulk-category').val();
        const quantity = parseInt($('#bulk-quantity').val());
        const message = $('#bulk-message').val().trim();

        // Client-side validation
        if (!name || !email || !phone || !category || !quantity || !message) {
            Swal.fire({
                icon: 'error',
                title: 'Missing Information',
                text: 'Please fill in all required fields.',
                confirmButtonColor: '#000'
            });
            return;
        }

        // Email validation
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailRegex.test(email)) {
            Swal.fire({
                icon: 'error',
                title: 'Invalid Email',
                text: 'Please enter a valid email address.',
                confirmButtonColor: '#000'
            });
            return;
        }

        // Quantity validation
        if (quantity < 50) {
            Swal.fire({
                icon: 'warning',
                title: 'Minimum Quantity',
                text: 'Minimum order quantity is 50 units.',
                confirmButtonColor: '#000'
            });
            return;
        }

        // Show loading
        Swal.fire({
            title: 'Submitting...',
            text: 'Please wait while we process your request.',
            allowOutsideClick: false,
            didOpen: () => {
                Swal.showLoading();
            }
        });

        // AJAX submission
        $.ajax({
            url: 'actions/process-bulk-order.php',
            type: 'POST',
            data: {
                name: name,
                email: email,
                phone: phone,
                company: company,
                category: category,
                quantity: quantity,
                message: message
            },
            dataType: 'json',
            success: function (response) {
                if (response.success) {
                    Swal.fire({
                        icon: 'success',
                        title: 'Quote Request Submitted!',
                        html: `Thank you for your interest in bulk ordering.<br>
                               <strong>Confirmation emails have been sent to:</strong><br>
                               • Your email: <strong>${email}</strong><br>
                               • Our team for processing<br><br>
                               We will contact you within 24 hours.`,
                        confirmButtonColor: '#000'
                    }).then(() => {
                        $('#bulk-order-form')[0].reset();
                    });
                } else {
                    Swal.fire({
                        icon: 'error',
                        title: 'Submission Failed',
                        text: response.message || 'Something went wrong. Please try again.',
                        confirmButtonColor: '#000'
                    });
                }
            },
            error: function () {
                Swal.fire({
                    icon: 'error',
                    title: 'Server Error',
                    text: 'Unable to submit your request. Please try again later or contact us directly.',
                    confirmButtonColor: '#000'
                });
            }
        });
    });
</script>

